package dto;

public class TaskCreator {
    String username;
    String name;
    public TaskCreator() {
    }
    public TaskCreator(String username) {
        this.username = username;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
